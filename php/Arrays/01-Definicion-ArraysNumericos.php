ARRAYS
------

Un Array es un tipo de dato en PHP que es capaz de almacenar al 
mismo tiempo muchos otros datos de cualquier tipo: números, cadenas, 
booleans, otros arrays, ...

	$a = array();   ----> Este sería un array vacío

Los elementos de un array se escriben entre los paréntesis y separados 
por comas:

	$dias = array("Lunes","Martes","Miércoles","Jueves","Viernes","Sábado","Domingo");
	
	$numeros = array(21,-3,34.2,0,98765);
	
	$mixto = array("Hola",43,true,-8,"Adiós");
	
La forma que tiene un array de almacenar los datos es ordenada e 
indexada, es decir, que cada elemento guardado en el array lleva 
asociado un índice que nos permitirá acceder a él en cualquier 
momento. Este índice, por defecto, es un número, de ahí que los 
arrays que crea PHP por defecto se denominan ARRAYS NUMÉRICOS.

La primera posición de un array, por defecto, tiene índice CERO.

La LONGITUD DE UN ARRAY es el número de elementos que contiene.

Al ser la primera posición la 0, la última posición de un array 
ordenado sera siempre la posición LONGITUD - 1.

