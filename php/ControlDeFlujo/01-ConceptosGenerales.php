CONTROL DE FLUJO
----------------

Hasta ahora nuestros programas han sido completamente lineales y las sentencias 
se han ido ejecutando secuencialmente, de arriba a abajo, conforme han ido 
apareciendo en el código.

Este se considera el flujo natural del programa. Pero la gran mayoría de las veces 
esto no ocurre así ya que puede haber alteraciones que varíen la ejecución de un 
programa, viéndose alterado en estos casos el FLUJO del programa:

	- Consideramos Flujo de un programa a cómo se ejecuta dicho programa.
	
Imaginemos, por ejemplo, que solicitamos la edad para controlar el acceso a una 
web. Existen dos posibilidades: que tenga 18 años o más y permitamos el acceso o 
que no los tenga y se lo neguemos.

Para controlar estas alteraciones del flujo natural del programa existen los 
denominados OPERADORES DE CONTROL DE FLUJO.

El uso de estos operadores requiere de una sintaxis característica y bien definida 
que se denomina ESTRUCTURA DE CONTROL DE FLUJO.

